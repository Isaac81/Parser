package components.variables;

public abstract class Variable {
    protected String name;
    protected int scope;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getScope() {
        return scope;
    }

    public void setScope(int scope) {
        this.scope = scope;
    }

    public abstract String toString();
}
